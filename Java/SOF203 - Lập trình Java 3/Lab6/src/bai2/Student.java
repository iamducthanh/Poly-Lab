package bai2;

public class Student {
	String name;
	String address;
	String parentName;
	String contactNo;
	String standard;
	String fees;
	
	public Student(String name, String address, String parentName, String contactNo, String standard, String fees) {
		super();
		this.name = name;
		this.address = address;
		this.parentName = parentName;
		this.contactNo = contactNo;
		this.standard = standard;
		this.fees = fees;
	}
	
	
}
